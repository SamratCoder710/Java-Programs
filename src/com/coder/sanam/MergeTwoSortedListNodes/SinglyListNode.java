package com.coder.sanam.MergeTwoSortedListNodes;

public class SinglyListNode {
    int val;
    SinglyListNode next;

    SinglyListNode() {
    }

    SinglyListNode(int val) {
        this.val = val;
    }

    SinglyListNode(int val, SinglyListNode next) {
        this.val = val;
        this.next = next;
    }
}
